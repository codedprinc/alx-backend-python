#!/usr/bin/env python3
"""
Task 1 Basic annotations - concat

a type-annotated function concat that takes a string str1 and
a string str2 as arguments and returns a concatenated string

"""


def concat(str1: str, str2: str) -> str:
    """
    concat function:

    Args:
      str1 (str): First string to be concatenated.
      str2 (str): Second string to be concatenated.

    Returns:
      str: A string that is a result from concatenating


    """
    return str1 + str2
