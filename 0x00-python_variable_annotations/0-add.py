#!/usr/bin/env python3
"""
Task 0 - Basic annotations - add

 type-annotated function add that takes a float a and
 a float b as arguments and returns their sum as a float.
"""


def add(a: float, b: float) -> float:
    """
    Add function

    Args:
      a (float): The first parameter.
      b (float): The second parameter.

    Returns:
      float: type value of the addition of `a` and `b`

    """
    return a + b
